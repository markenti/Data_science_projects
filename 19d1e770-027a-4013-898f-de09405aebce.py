def genre_weekday(table, day, time1, time2):
    genre_df = table[
        (table['day'] == day) & 
        (table['time'] > time1) & 
        (table['time'] < time2)
    ]
    genre_df_count = genre_df.groupby('genre')['genre'].count()
    genre_df_sorted = genre_df_count.sort_values(ascending=False)
    return genre_df_sorted.head(10)


